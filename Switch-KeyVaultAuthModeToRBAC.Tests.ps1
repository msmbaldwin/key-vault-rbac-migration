<#
.SYNOPSIS
Test runner for KvRbacMigrator test suite

.DESCRIPTION
Simple test runner that executes the comprehensive test suite.

.EXAMPLE
# Run all tests
.\Run-Tests.ps1
#>

Write-Host "KvRbacMigrator Test Runner" -ForegroundColor Cyan
Write-Host "============================" -ForegroundColor Cyan

# Check PowerShell version
if ($PSVersionTable.PSVersion.Major -lt 5) {
    Write-Error "PowerShell 5.0 or higher is required"
    exit 1
}

# Check for Pester module
if (-not (Get-Module -ListAvailable -Name Pester)) {
    Write-Warning "Pester 5 module not found. Please run 'Install-Module Pester -Force -SkipPublisherCheck -Scope CurrentUser' to install."
}

# Verify required files exist
$parentDir = Split-Path $PSScriptRoot -Parent
$requiredFiles = @(
    @{ Path = "RoleMapping.Tests.ps1"; Location = $PSScriptRoot },
    @{ Path = "RoleMapping.json"; Location = $parentDir },
    @{ Path = "Common.ps1"; Location = $parentDir },
    @{ Path = "Invoke-KvRbacAnalysis.ps1"; Location = $parentDir }
)

foreach ($fileInfo in $requiredFiles) {
    $fullPath = Join-Path $fileInfo.Location $fileInfo.Path
    if (-not (Test-Path $fullPath)) {
        Write-Error "Required file not found: $fullPath"
        exit 1
    }
}

Write-Host "All required files found" -ForegroundColor Green

# Set execution policy if needed (for current process only)
try {
    Set-ExecutionPolicy -ExecutionPolicy Bypass -Scope Process -Force
}
catch {
    Write-Warning "Could not set execution policy: $($_.Exception.Message)"
}

# Execute the test suite
Write-Host "Executing test suite..." -ForegroundColor Cyan
Write-Host ""

try {
    Import-Module Pester -Force
    # Run all tests with detailed progress
    $pesterConfig = [PesterConfiguration]::Default
    $pesterConfig.Run.Path = @(Join-Path $PSScriptRoot "*.Tests.ps1")
    $pesterConfig.Output.Verbosity = 'Detailed'
    $pesterConfig.Run.PassThru = $true
    $results = Invoke-Pester -Configuration $pesterConfig
    
    if ($results.FailedCount -eq 0) {
        Write-Host ""
        Write-Host "Test execution completed successfully!" -ForegroundColor Green
        exit 0
    }
    else {
        Write-Host ""
        Write-Host "Some tests failed. Exit code: $($results.FailedCount)" -ForegroundColor Red
        exit $results.FailedCount
    }
}
catch {
    Write-Host "Test execution failed with error: $($_.Exception.Message)" -ForegroundColor Red
    exit 1
}


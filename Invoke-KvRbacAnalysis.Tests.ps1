#Requires -Modules Pester
Describe "Debug Test" {
    BeforeAll {
        . "$PSScriptRoot/../Common.ps1"
        . "$PSScriptRoot/../Invoke-KvRbacAnalysis.ps1"
        Initialize-AuditLogging
        $script:mockRoleMapping = @{
            roleDefinitions = @{ "Key Vault Reader" = @{ id = "1"; weight = 10 }; "Key Vault Secrets User" = @{ id = "2"; weight = 20 }; "Key Vault Crypto User" = @{ id = "3"; weight = 20 } }
            administratorModeMapping = @{ requiredObjectTypes = @("secrets", "keys", "certificates") }
            rolePermissions = @{ "Key Vault Administrator" = @{ secrets = @("get","list","set","delete"); keys = @("get","list","create"); certificates = @("get","list","create") } }
            secrets = @{ "get" = "Key Vault Secrets User"; "list" = "Key Vault Secrets User" }
            keys = @{ "get" = "Key Vault Crypto User"; "list" = "Key Vault Crypto User" }
            certificates = @{}
        }
    }
    It "Debug" {
        $script:MigrationContext.Metrics = @{ StartTime = Get-Date; VaultsProcessed = 0; VaultsSkipped = 0; VaultsFailed = 0; PrincipalsAnalyzed = 0; RoleAssignmentsGenerated = 0; WarningsGenerated = 0; ErrorsEncountered = 0; PermissionMappingDecisions = @(); UnmappedPermissions = @() }
        $mockVault = [PSCustomObject]@{ name = 'v1'; resourceGroup = 'rg1'; subscriptionId = 'sub1'; id = '/subscriptions/sub1/resourceGroups/rg1/providers/Microsoft.KeyVault/vaults/v1' }
        $mockPolicies = @([PSCustomObject]@{ ObjectId = 'p1'; PermissionsToSecrets = @("get","list"); PermissionsToKeys = @(); PermissionsToCertificates = @() })
        Mock Get-VaultAccessPolicies { return $mockPolicies }
        Mock Get-ExistingRoleAssignments { return @() }
        Mock Get-ResolvedPrincipal { return [PSCustomObject]@{ PrincipalId = $PrincipalId; DisplayName = 'Test'; PrincipalType = 'User'; AppId = $null; UserPrincipalName = $null } }
        $results = Invoke-VaultAnalysis -Vaults @($mockVault) -RoleMapping $script:mockRoleMapping
        Write-Host "Results type: $($results.GetType().FullName)"
        Write-Host "Results count: $(@($results).Count)"
        if ($results -is [array]) {
            foreach ($r in $results) { Write-Host "  Item type: $($r.GetType().FullName), Keys: $($r.Keys -join ',')" }
        } elseif ($results -is [hashtable]) {
            Write-Host "  Keys: $($results.Keys -join ',')"
        }
        @($results).Count | Should -Be 1
    }
}

